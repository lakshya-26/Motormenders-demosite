<p><h2>Body-Shop (Denting & Painting)</h2></p>
<br>

We provide A-grade painting services across all types and categories of cars available in the market.<br>
It could be a panel of your new car, a complete outer body paint job for a decently aged vehicle, a custom colour paint job f your choice or maybe even a hardcore restoration of your old family’s car; we are here to help you out.<br>
Our experienced staff puts utmost attention in bringing out the best possible finish on your car’s paint.<br>
We diligently follow all required protocols and focus on delivering an exceptional experience that you probably might not have had at body-shop before. Our paint jobs come with a 2 year application warranty and if taken good care of, the paint can easily last you a decade.<br>
Choose from a range of Body, Wheel, Glasses, Plastic trim coatings and Paint protection films after the Paint job, here at Brotomotiv. Full body paint jobs get discounts on detailing and paint protection services.<br>
Contact us by filling out the enquiry form and get your painted today.<br>
<b>Note:</b><br>
•    Heavily rusted vehicles or vehicle which have been painted multiple times before will not be covered under warranty.<br>
•    We provide a 6 month warranty on Rusting, 1 year warranty on Blistering, Cracking, Peeling & Paint adhesion, and a 3 year warranty on Clear coat failure and/or Paint failure.<br>
•    Paint shrinkage is normal and acceptable on cars with heavy base work. However, cracking and peeling of paint in such cars is still covered under warranty.<br>
•    Life of the paint is totally dependant on the usage and maintenance by the customer.<br>
