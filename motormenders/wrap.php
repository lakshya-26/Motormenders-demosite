<p><h2>Wrap Jobs</h2></p>
<br>

Vinyl wrapping brings a new custom look to your car, without tampering the OEM paint job.<br>
We provide wrapping services with almost 300+ colours and shades available with the best in league finish and application.<br>
It could be a simple sports stripe across the bonnet, a dual tone look for your new car or maybe a complete colour swap with a fascinating shade that your OEM supplier couldn’t offer, we are here to help.<br>
Our wrap jobs come with a 2 year application warranty.<br>
This service is usually recommended to car which are good in shape and want to do something different, at the same time keeping the original paint intact.<br>
Choose from a range of Wheel, Glasses and Plastic trim coatings along with your wrap job, here at MotorMenders.<br>
Contact us by filling out the enquiry form and get your car ceramic coated today.<br>
<b>Note:</b><br>
•    We use only the best in class material for wrapping.<br>
•    Changing colour requires correction on the car’s RC, through your local RTO.<br>
•    Post care is utmost necessary with wrapping. Pressure washing and aggressive cleaning may tamper the edges and result in opening of the film from certain areas.<br>
•    Application warranty covers natural peeling of the material. Customer’s mishandling or abuse of the material shall not be covered under warranty.<br>
