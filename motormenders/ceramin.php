<p><h2>Ceramic and Graphene Coatings</h2></p>
<br>
Advanced coatings derived with nano-technology that help prevent environmental damages on your car’s precious paint.<br>
These coatings form a 9H or 10H hard crystallised layer on top of your vehicle’s clear coat and provide resistance against light scratches, chemical / acids and also protects the paint against harmful UV rays.<br> 
You will also experience hydrophobic properties with the application of these coatings, which would avoid water markings to a great extent.<br>
These coatings are available with 3, 5 and 7 year application warranty plans.<br>
Choose from a range of Body, Wheel, Glasses and Plastic trim coatings here at MotorMenders.<br>
Contact us by filling out the enquiry form and get your car ceramic coated today.<br>
<b>Note:</b><br>
•    The life of coatings totally depend on the usage and handling of the vehicle.<br>
•    No coating is scratch-proof and daily care is utmost necessary for the best appearance of your vehicle.<br>
•    Ceramic coatings are built to resist light damages. To get the best life out of a ceramic coat, use clean microfibres and less TDS water for daily wiping and washing needs.<br>
•    An annual maintenance is necessary for ceramic coated cars. This service is payable and helps get rid of damages and contaminants as much as possible.<br>
•    Maintenance charges may change as per inflation and fluctuation in material pricing and its periodicity depends purely on usage and daily care from the customer’s end.<br>
•    Discolouration, Patchiness and Peeling / Cracking of coating is covered under application warranty. Ceramic coating’s warranty is not applicable on 3rd party painted panels.<br>
